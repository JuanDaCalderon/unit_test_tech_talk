export class Calculator {

  /**
   * This adds two numbers, a and b
   * @param {number} a
   * @param {number} b
   * @returns {number} a + b
   */
  add(a, b) {
    return a + b;
  }

  /**
   * This substracts two numbers, a and b
   * @param {number} a
   * @param {number} b
   * @returns {number} a - b
   */
  sub(a, b) {
    return a - b;
  }
}
